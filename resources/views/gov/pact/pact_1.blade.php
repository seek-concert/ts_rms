    <span style="font-size:21px;font-family:仿宋">2.</span><span style="font-size:21px;font-family:仿宋">不隐瞒被征收房屋的产权纠纷或抵押担保等状况，隐瞒或提供材料不实产生的法律责任由乙方承担。</span>
</p>
<p style="text-indent:44px;line-height:37px">
    <strong><span style="font-size:21px;font-family:仿宋">八、违约责任</span></strong>
</p>
<p style="text-indent:44px;line-height:37px">
    <span style="font-size:21px;font-family:仿宋">本协议生效后，双方应共同遵守，如有一方违约或造成对方损失者，必须赔偿损失和承担违约责任。</span>
</p>
<p style="text-indent:44px;line-height:37px">
    <span style="font-size:21px;font-family:仿宋">1.</span><span style="font-size:21px;font-family:仿宋">因甲方的原因（不可抗拒因素除外）未按协议约定向乙方支付征收补偿款的，甲方应当承担逾期支付的民事责任，按未支付金额每日万分之<strong><span style="text-decoration:underline;">三</span></strong>的比例支付违约金。</span>
</p>
<p style="text-indent:44px;line-height:37px">
    <span style="font-size:21px;font-family:仿宋">2.</span><span style="font-size:21px;font-family:仿宋">乙方未按期向甲方缴纳（结清）产权调换房屋差价或未在约定的期限内完成搬迁并移交房屋及房屋权属证等，乙方应当承担违约责任，甲方有权在乙方的征收补偿所得中按照每日万分之<strong><span style="text-decoration:underline;">三</span></strong>的比例扣除。</span>
</p>
<p style="text-indent:44px;line-height:37px">
    <strong><span style="font-size:21px;font-family:仿宋">九、其它条款</span></strong>
</p>
<p style="text-indent:44px;line-height:37px">
    <span style="font-size:21px;font-family:仿宋">1.</span><span style="font-size:21px;font-family:仿宋">自本协议签订后，乙方须在甲方规定的期限内将被征收房屋及宅院内的财物搬出，并及时将所有的建筑物、构筑物及地上附着物移交给甲方，乙方不得擅自拆除。如乙方擅自拆除，甲方有权从乙方补偿款中作价扣除，并由乙方自行承担擅自拆除造成的一切后果：纠纷、违约、安全问题等。</span>
</p>
<p style="text-indent:42px;line-height:38px">
    <span style="font-size:21px;font-family:仿宋">2.</span><span style="font-size:21px;font-family:仿宋">乙方搬迁结束后，经甲方验收合格，一次性向乙方兑付征收补偿资金。</span>
</p>
<p style="text-indent:42px;line-height:38px">
    <span style="font-size:21px;font-family:仿宋">3.</span><span style="font-size:21px;font-family:仿宋">被征收房屋交付前产生的水费、电费、物业费、电话费、有线电视费等相关费用由乙方自行承担，并负责结清所产生的相关费用。</span>
</p>
<p style="text-indent:42px;line-height:38px">
    <span style="font-size:21px;font-family:仿宋">4.</span><span style="font-size:21px;font-family:仿宋">本协议签订之日，乙方须将被征收房屋产权证原件等相关证明材料交付给甲方，由甲方统一到房屋登记等部门办理房屋产权证的登记注销手续。如乙方不及时交付或拒绝交付，造成后续产权无法办理或产生的一切责任，均由乙方自行承担。</span>
</p>
<p style="text-indent:42px;line-height:38px">
    <span style="font-size:21px;font-family:仿宋">5.</span><span style="font-size:21px;font-family:仿宋">房款的交纳及安置房屋入住手续的办理。甲乙双方结清以上各项费用后，甲方为乙方出具相关入住证明，并协助乙方办理有关入住手续。</span>
</p>
<p style="text-indent:43px;line-height:38px">
    <strong><span style="font-size:21px;font-family:仿宋">十、争议的处理</span></strong>
</p>
<p style="text-indent:42px;line-height:38px">
    <span style="font-size:21px;font-family:仿宋">本协议在履行过程中如发生争议，双方应先行协商解决，协商不成，可向有管辖权的人民法院提起诉讼。</span>
</p>
<p style="text-indent:43px;line-height:38px">
    <strong><span style="font-size:21px;font-family:仿宋">十一、协议生效及持有</span></strong>
</p>
<p style="text-indent:43px;line-height:37px">
    <span style="font-size:21px;font-family:仿宋">本协议自甲乙双方签订之日起生效。本协议一式六份，甲方执五份，乙方执一份，具有同等法律效力。</span>
</p>
